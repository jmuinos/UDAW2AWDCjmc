// Tabla de multiplicar del 7 usando bucle for
console.log("Tabla de multiplicar del 7:");
for (let i = 1; i <= 10; i++) {
    console.log(`7 x ${i} = ${7 * i}`);
}

// Tabla de sumar del 8 usando bucle while
console.log("\nTabla de sumar del 8:");
let j = 1;
while (j <= 10) {
    console.log(`8 + ${j} = ${8 + j}`);
    j++;
}

// Tabla de dividir del 9 usando bucle do...while
console.log("\nTabla de dividir del 9:");
let k = 1;
do {
    console.log(`9 / ${k} = ${9 / k}`);
    k++;
} while (k <= 10);
